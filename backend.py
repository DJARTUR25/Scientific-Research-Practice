        table.setColumnCount(num_x + 5)  # +5 –¥–ª—è –Ω–æ–º–µ—Ä–∞ —Å–ª–æ—è, t, max|u2-u|, i_max, —Å—Ä–µ–¥–Ω–µ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ

