        headers = ["‚Ññ —Å–ª–æ—è", "t", "max|u2 - u|", "i_max", "–°—Ä–µ–¥–Ω–µ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ"]
        for col, header in enumerate(headers):
            table.setHorizontalHeaderItem(col, QTableWidgetItem(header))
